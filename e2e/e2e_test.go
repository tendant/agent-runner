func TestE2E_ApiKeyAuth(t *testing.T) {
	if testing.Short() {
		t.Skip("skipping E2E test in short mode")
	}

	baseDir := t.TempDir()
	reposDir := filepath.Join(baseDir, "repos")
	os.MkdirAll(reposDir, 0755)
	os.MkdirAll(filepath.Join(reposDir, "test-project", ".git"), 0755)

	cfg := &config.Config{
		ReposRoot:             reposDir,
		LogsRoot:                 filepath.Join(baseDir, "logs"),
		TmpRoot:                  filepath.Join(baseDir, "tmp"),
		AllowedProjects:          []string{},
		MaxRuntimeSeconds:        30,
		MaxConcurrentJobs:        5,
		GitPushRetries:           1,
		GitPushRetryDelaySeconds: 0,
		Validation: config.ValidationConfig{
			BlockBinaryFiles: false,
			BlockedPaths:     []string{},
		},
		API: config.APIConfig{
			Bind:   "127.0.0.1:0",
			APIKey: "secret-key-123",
		},
		Agent: config.AgentConfig{
			MaxIterations:       50,
			MaxTotalSeconds:     60,
			MaxIterationSeconds: 30,
			Author:              "claude-agent",
			CommitPrefix:        "[agent]",
		},
		JobRetentionSeconds:     3600,
		StartupCleanupStaleJobs: false,
	}

	srv := api.NewServer(cfg)
	ts := httptest.NewServer(srv.Handler())
	defer ts.Close()

	// Request without API key should fail
	body, _ := json.Marshal(map[string]interface{}{
		"project":     "test-project",
		"instruction": "do stuff",
		"paths":       []string{"src/"},
	})

	resp, err := http.Post(ts.URL+"/run", "application/json", bytes.NewReader(body))
	if err != nil {
		t.Fatalf("request failed: %v", err)
	}
	resp.Body.Close()

	if resp.StatusCode != http.StatusUnauthorized {
		t.Errorf("expected 401 without key, got %d", resp.StatusCode)
	}

	// Request with valid API key should succeed (202)
	req, _ := http.NewRequest(http.MethodPost, ts.URL+"/run", bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("X-API-Key", "secret-key-123")

	resp2, err := http.DefaultClient.Do(req)
	if err != nil {
		t.Fatalf("request failed: %v", err)
	}
	resp2.Body.Close()

	if resp2.StatusCode != http.StatusAccepted {
		t.Errorf("expected 202 with valid key, got %d", resp2.StatusCode)
	}
}

